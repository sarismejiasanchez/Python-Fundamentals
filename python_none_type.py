# None is a special data type in Python that represents the absence of a value or a null value. 
# It is often used to indicate that a variable has no value or that a function does not return anything.

x = None
print(x) # Output: None
print(type(x)) # Output: <class 'NoneType'>

print(type("")) # Output: <class 'str'>
print(type(0)) # Output: <class 'int'>
print(type([])) # Output: <class 'list'>
print(type({})) # Output: <class 'dict'>
print(type(())) # Output: <class 'tuple'>
print(type(False)) # Output: <class 'bool'>