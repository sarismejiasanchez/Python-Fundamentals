x = "This is a Python variable."
print(x) # Output the value of the variable x

# Python permits reassigning variables to new values
x = "This is a new value for the variable."
print(x) # Output the updated value of the variable x

# Python variable names are case-sensitive
X = "This is a different variable due to case sensitivity."
print(X) # Output the value of the variable X
print(x) # Output the value of the variable x again to show it remains unchanged

